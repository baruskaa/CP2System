    private void jbtnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDeleteActionPerformed
        DefaultTableModel model= (DefaultTableModel) jTable_1.getModel();
        if (jTable_1.getSelectedRow() == -1){
            if (jTable_1.getRowCount() == 0){
            
                JOptionPane.showMessageDialog(null, "No data to delete ","Reservation History System",
                JOptionPane.OK_OPTION) ;
                
            }else{
                
                JOptionPane.showMessageDialog(null, "Select a row to delete ", "Reservation History System", 
                JOptionPane.OK_OPTION) ;
                
            }
        }else{
            model.removeRow(jTable_1.getSelectedRow());
        }
    }//GEN-LAST:event_jbtnDeleteActionPerformed

