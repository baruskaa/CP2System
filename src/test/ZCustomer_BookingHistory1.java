    private void jbtn_searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_searchActionPerformed
       if (jDateChooser1.getDate() == null) {
        JOptionPane.showMessageDialog(null, "Please select a date");
        return;
    }

    // Match the format used in the JTable
    SimpleDateFormat sDate = new SimpleDateFormat("MMM d, yyyy");
    String date = sDate.format(jDateChooser1.getDate());

    DefaultTableModel model = (DefaultTableModel) jTable_1.getModel();
    TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
    jTable_1.setRowSorter(sorter);

    // Filter ONLY Date Booked column
    sorter.setRowFilter(RowFilter.regexFilter("^" + date + "$", 10));
    }//GEN-LAST:event_jbtn_searchActionPerformed

